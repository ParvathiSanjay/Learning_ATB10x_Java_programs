package ex_03_Java_Operators;

public class Decrement_operator {
    public static void main(String[] args) {
         int a =10;
        int res=a--;
        System.out.println(res);
        System.out.println(a);

        int b =10;
        int res1=--b;
        System.out.println(res1);
        System.out.println(b);
    }
}
