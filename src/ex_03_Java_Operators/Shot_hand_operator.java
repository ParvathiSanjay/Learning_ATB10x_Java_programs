package ex_03_Java_Operators;

public class Shot_hand_operator {
    public static void main(String[] args) {
        int a=10;

      a+=5; // a=a+5;

        System.out.println(a);
       a-=5;// a=a-5;
        System.out.println(a);
        a*=2;
        System.out.println(a);


        a/=2;
        System.out.println(a);
    }
}
