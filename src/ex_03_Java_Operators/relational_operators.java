package ex_03_Java_Operators;

public class relational_operators {
    public static void main(String[] args) {
        int a=20 , b=20;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println(a==b);
        System.out.println(a!=b);

    }
}
